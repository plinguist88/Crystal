package Model.DTO;

public class CandidateInfo { //지원자 정보 저장
	String participantNum;
	String participantName;
	String participantBirth;
	String participantGender;
	String participantPh;
	String participantAddr;
	public String getParticipantNum() {
		return participantNum;
	}
	public void setParticipantNum(String participantNum) {
		this.participantNum = participantNum;
	}
	public String getParticipantName() {
		return participantName;
	}
	public void setParticipantName(String participantName) {
		this.participantName = participantName;
	}
	public String getParticipantBirth() {
		return participantBirth;
	}
	public void setParticipantBirth(String participantBirth) {
		this.participantBirth = participantBirth;
	}
	public String getParticipantGender() {
		return participantGender;
	}
	public void setParticipantGender(String participantGender) {
		this.participantGender = participantGender;
	}
	public String getParticipantPh() {
		return participantPh;
	}
	public void setParticipantPh(String participantPh) {
		this.participantPh = participantPh;
	}
	public String getParticipantAddr() {
		return participantAddr;
	}
	public void setParticipantAddr(String participantAddr) {
		this.participantAddr = participantAddr;
	}
	
	
}
