package Command;

public class RegContractorCommand {
	String contractorId;
	String contractorPw;
	String contractorName;
	String contractorGender;
	String contractorBirth;
	String contractorPhone;
	String contractorAddr;
	String participantNum;
	Integer contractorLevel;
	
	public String getContractorId() {
		return contractorId;
	}
	public void setContractorId(String contractorId) {
		this.contractorId = contractorId;
	}
	public String getContractorPw() {
		return contractorPw;
	}
	public void setContractorPw(String contractorPw) {
		this.contractorPw = contractorPw;
	}
	public String getContractorName() {
		return contractorName;
	}
	public void setContractorName(String contractorName) {
		this.contractorName = contractorName;
	}
	public String getContractorGender() {
		return contractorGender;
	}
	public void setContractorGender(String contractorGender) {
		this.contractorGender = contractorGender;
	}
	public String getContractorBirth() {
		return contractorBirth;
	}
	public void setContractorBirth(String contractorBirth) {
		this.contractorBirth = contractorBirth;
	}
	public String getContractorPhone() {
		return contractorPhone;
	}
	public void setContractorPhone(String contractorPhone) {
		this.contractorPhone = contractorPhone;
	}
	public String getContractorAddr() {
		return contractorAddr;
	}
	public void setContractorAddr(String contractorAddr) {
		this.contractorAddr = contractorAddr;
	}
	public String getParticipantNum() {
		return participantNum;
	}
	public void setParticipantNum(String participantNum) {
		this.participantNum = participantNum;
	}
	public Integer getContractorLevel() {
		return contractorLevel;
	}
	public void setContractorLevel(Integer contractorLevel) {
		this.contractorLevel = contractorLevel;
	}
	
	
}
