package Command;

public class CarRequestCommand {
	private String employeeNum;
	private String from;
	private String to;
	private String carType;
	private String carPassengerNum;
	private String carRegNum1;
	
	public String getEmployeeNum() {
		return employeeNum;
	}
	public void setEmployeeNum(String employeeNum) {
		this.employeeNum = employeeNum;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getCarType() {
		return carType;
	}
	public void setCarType(String carType) {
		this.carType = carType;
	}
	public String getCarPassengerNum() {
		return carPassengerNum;
	}
	public void setCarPassengerNum(String carPassengerNum) {
		this.carPassengerNum = carPassengerNum;
	}
	public String getCarRegNum1() {
		return carRegNum1;
	}
	public void setCarRegNum1(String carRegNum1) {
		this.carRegNum1 = carRegNum1;
	}
}