package Command;

public class JumjuLoginCommand {

	private String id;
	private String pw;
	private Boolean jjIdStore;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public Boolean getJjIdStore() {
		return jjIdStore;
	}
	public void setJjIdStore(Boolean jjIdStore) {
		this.jjIdStore = jjIdStore;
	}
	
}
